#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;
	unsigned char	uc;
	size_t			i;

	str = (unsigned char *) s;
	uc = (unsigned char) c;
	i = -1;
	while (++i < n)
	{
		if (str[i] == uc)
			return ((void *) &str[i]);
	}
	return (NULL);
}
